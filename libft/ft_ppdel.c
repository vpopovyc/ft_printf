#include "includes/libft.h"

void	ft_ppdel(char ***sv)
{
	char		i;

	i = 0;
	if (*sv)
	{
		while (**sv)
		{
			ft_strdel(*sv);
			++(*sv);
			++i;
		}
		*sv -= i;
		free(*sv);
		*sv = NULL;
	}
}
