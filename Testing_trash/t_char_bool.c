#include <stdio.h>
#include <string.h>

int			main(void)
{
	char 	c;
	char	*str = strdup("#*");

	c = *str;
	if (c == '#')
	{
		printf("#\n");
		c = *(++str);
	}
	if (c == '*')
		printf("*\n");
	return (0);
}
