#include "ft_printf.h"

int		main(void)
{
	char *s = strdup("%-010.12-1s");
	int		 d = 0;
	int		 p = 0;
	char     f = 1;

	while (*(++s))
	{
		if ((*s >= '1' && *s <= '9') && f)
		{
			if (f == 1)
				d = ft_atosize_t(s);
			else if (f == 2)
				p = ft_atosize_t(s);
			f = 0;
		}
		else if (*s == '.')
			f = 2;
		else if (!(*s >= '1' && *s <= '9'))
			f = 1;
	}
	printf("d: %i\np: %i\n", d, p);
	return (0);
}
